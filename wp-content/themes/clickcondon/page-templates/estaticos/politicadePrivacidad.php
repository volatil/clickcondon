	<h1>POLITICA DE PRIVACIDAD</h1>

	<h2>¿QUÉ INFORMACIÓN SE RECOPILA?</h2>

	<p>
		Nosotros recopilamos información de usted cuando se registra en el sitio, hacer un pedido, participar en un concurso o sorteo, responde a una encuesta o comunicación, como el correo electrónico o participar en otra de las características del sitio. 
		Al solicitar o registrar, podemos pedirle su nombre, dirección de correo electrónico, número de teléfono, u otra información usted. 
		Es posible, sin embargo, visitar nuestro sitio de forma anónima. 
		También recopilamos información acerca de los destinatarios para que podamos cumplir con la compra. 
		La información que recopilamos acerca de los destinatarios no se utiliza para fines de marketing. 
		Al igual que muchos sitios web, utilizamos "cookies" para mejorar su experiencia y recabar información sobre los visitantes y las visitas a nuestros sitios web. 
	</p>

	<h2>¿CÓMO PROTEGEMOS LA INFORMACIÓN DEL VISITANTE?</h2>
	<p>
		Implementamos una variedad de medidas de seguridad para mantener la seguridad de su información personal. 
		Su información personal se guarda bajo redes seguras y sólo es accesible por un número limitado de personas con derechos especiales de acceso a dichos sistemas, y están obligados a mantener la información confidencial. 
		Para realizar pedidos o acceder a su información personal, ofrecemos el uso de un servidor seguro. 
		Toda la información confidencial / de crédito que usted suministra se transmite a través de la tecnología Secure Socket Layer (SSL) y se encripta en nuestras bases de datos para ser visitada sólo como se indica más arriba.
	</p>

	<h2>PREGUNTAS Y COMENTARIOS</h2>
	<p>
		Agradecemos sus preguntas, comentarios y preocupaciones acerca de la privacidad. 
		Por favor envíenos cualquier y todos los comentarios relacionados con la privacidad, o cualquier otro tema.
	</p>

	<h2>SU CONSENTIMIENTO</h2>
	<p>
		Al utilizar nuestro sitio, usted acepta nuestra política de privacidad.	
	</p>

